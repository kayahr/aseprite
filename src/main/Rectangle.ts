/** A rectangle. */
export interface Rectangle {
    /** The left edge of the rectangle. */
    x: number;

    /** The top edge of the rectangle. */
    y: number;

    /** The rectangle width. */
    w: number;

    /** The rectangle height. */
    h: number;
}
